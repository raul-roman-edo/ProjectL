package es.tid.pdg.gdx.core.actor;

public class ColorRGB {
    public static final float DEFAULT = 1;
    private float red = DEFAULT;
    private float green = DEFAULT;
    private float blue = DEFAULT;

    public float getRed() {
        return red;
    }

    public float getGreen() {
        return green;
    }

    public float getBlue() {
        return blue;
    }
}
